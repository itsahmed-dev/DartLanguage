// Dart - Arithmetic Operators

// Pre and Post Increment and Decrement Operator

void main() {
  print('Post Increment:');
  var postIncrement = 5;
  print(postIncrement++);
  print(postIncrement);

  print(''); //  Line-Break

  print('Post Decrement');
  var postDecrement = 5;
  print(postDecrement--);
  print(postDecrement);

  print(''); //  Line-Break

  print('Pre Increment:');
  var preIncrement = 5;
  print(++preIncrement);

  print(''); //  Line-Break

  print('Pre Decrement');
  var preDecrement = 5;
  print(--preDecrement);
}
