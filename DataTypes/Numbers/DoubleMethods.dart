// Dart - Data Types: Numbers

// Double Methods

void main() {
  double firstNumber = 5.5;
  // double secondNumber = 10.9;
  // double thirdNumber = 20.20;

  int ceiledValue = firstNumber.ceil();
  print('Ceil of $firstNumber: $ceiledValue');
  int floorValue = firstNumber.floor();
  print('Floor of $firstNumber: $floorValue');
  int roundValue = firstNumber.round();
  print('Round of $firstNumber: $roundValue');
  String stringValue = firstNumber.toString();
  print('String of $firstNumber: $stringValue');
}
